export type TranslationKeys = keyof typeof T.es
export type Translations = typeof T.es

export function getTranslations(lang: Lang): Translations {
  return T[lang] ?? T.es
}

export function detectLang(acceptLanguage?: string): Lang {
  const sources = acceptLanguage
    ? acceptLanguage.split(',').map(l => l.split(';')[0].trim().toLowerCase())
    : []

  for (const loc of sources) {
    if (LOCALE_MAP[loc]) return LOCALE_MAP[loc]
    const short = loc.split('-')[0]
    if (LOCALE_MAP[short]) return LOCALE_MAP[short]
  }
  return 'es'
}
